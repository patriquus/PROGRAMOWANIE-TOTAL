public class JavaApplication6 {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        int a = 5, b = 10;
        System.out.println("a = " + a);      
        System.out.println("b = " + b);     
        System.out.println("a + b = " + (a+b));
        System.out.println("a - b = " + (a-b));
        System.out.println("a * b = " + (a*b));
        System.out.println("a / b = " + (a/b));        
        System.out.println("a % b = " + (a%b));    
        
    }
}
